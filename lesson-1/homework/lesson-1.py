# 1. Given a side of a square, find its perimeter and area.
side = 5  # example value
perimeter = 4 * side
area = side ** 2
print("Square:")
print("Perimeter:", perimeter)
print("Area:", area)

# 2. Given diameter of a circle, find its length (circumference).
import math
diameter = 10  # example value
circumference = math.pi * diameter
print("\nCircle:")
print("Circumference:", circumference)

# 3. Given two numbers a and b, find their mean.
a = 8
b = 12
mean = (a + b) / 2
print("\nMean of a and b:")
print("Mean:", mean)

# 4. Given two numbers a and b, find their sum, product, and square of each number.
sum_ab = a + b
product_ab = a * b
square_a = a ** 2
square_b = b ** 2
print("\nOperations on a and b:")
print("Sum:", sum_ab)
print("Product:", product_ab)
print("Square of a:", square_a)
print("Square of b:", square_b)
